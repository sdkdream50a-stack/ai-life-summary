// Language names for display
const languageNames = {
    en: "English",
    ko: "",
    ja: "",
    zh: "",
    es: "Espaol"
};

// Language flags/icons
const languageFlags = {
    en: "",
    ko: "",
    ja: "",
    zh: "",
    es: ""
};

/**
 * Get current language from localStorage or browser
 */
function getCurrentLanguage() {
    // Check localStorage first
    const saved = localStorage.getItem('ai-life-summary-lang');
    if (saved && translations[saved]) {
        return saved;
    }

    // Check browser language
    const browserLang = navigator.language.split('-')[0];
    if (translations[browserLang]) {
        return browserLang;
    }

    // Default to English
    return 'en';
}

/**
 * Set current language
 */
function setLanguage(lang) {
    if (!translations[lang]) {
        console.warn(`Language ${lang} not supported`);
        return;
    }

    localStorage.setItem('ai-life-summary-lang', lang);
    applyTranslations(lang);
    updateLanguageSelector(lang);
    updateLangSelectorDisplay(lang);

    // Update HTML lang attribute
    document.documentElement.lang = lang;
}

/**
 * Update language selector button display
 */
function updateLangSelectorDisplay(lang) {
    const flagEl = document.getElementById('current-lang-flag');
    const nameEl = document.getElementById('current-lang-name');
    if (flagEl) flagEl.textContent = languageFlags[lang] || languageFlags.en;
    if (nameEl) nameEl.textContent = languageNames[lang] || languageNames.en;
}

/**
 * Get translation for a key
 */
function t(key, lang = null) {
    const currentLang = lang || getCurrentLanguage();

    // Try current language
    if (translations[currentLang] && translations[currentLang][key]) {
        return translations[currentLang][key];
    }

    // Fallback to English
    if (translations.en && translations.en[key]) {
        return translations.en[key];
    }

    // Return key if not found
    return key;
}

/**
 * Apply translations to all elements with data-i18n attribute
 */
function applyTranslations(lang = null) {
    const currentLang = lang || getCurrentLanguage();

    // Translate elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        const translation = t(key, currentLang);

        if (element.tagName === 'INPUT' && element.type !== 'submit') {
            element.placeholder = translation;
        } else if (element.tagName === 'INPUT' && element.type === 'submit') {
            element.value = translation;
        } else {
            element.textContent = translation;
        }
    });

    // Translate elements with data-i18n-placeholder
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        element.placeholder = t(key, currentLang);
    });

    // Translate elements with data-i18n-title
    document.querySelectorAll('[data-i18n-title]').forEach(element => {
        const key = element.getAttribute('data-i18n-title');
        element.title = t(key, currentLang);
    });

    // Translate elements with data-i18n-aria-label
    document.querySelectorAll('[data-i18n-aria-label]').forEach(element => {
        const key = element.getAttribute('data-i18n-aria-label');
        element.setAttribute('aria-label', t(key, currentLang));
    });
}

/**
 * Update language selector display
 */
function updateLanguageSelector(lang) {
    // Use updateLangSelectorDisplay which preserves the span structure
    updateLangSelectorDisplay(lang);
}

/**
 * Create language selector HTML
 */
function createLanguageSelector() {
    const currentLang = getCurrentLanguage();

    return `
        <div id="language-selector" class="relative">
            <button type="button" class="flex items-center space-x-1 px-3 py-2 text-sm text-gray-600 hover:text-primary transition rounded-lg hover:bg-gray-100">
                ${languageFlags[currentLang]} ${languageNames[currentLang]}
                <svg class="w-4 h-4 ml-1" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                    <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M19 9l-7 7-7-7"/>
                </svg>
            </button>
            <div id="language-dropdown" class="hidden absolute right-0 mt-2 w-40 bg-white rounded-lg shadow-lg border border-gray-200 py-2 z-50">
                ${Object.keys(languageNames).map(lang => `
                    <button type="button" onclick="setLanguage('${lang}')" class="w-full px-4 py-2 text-left text-sm hover:bg-gray-100 flex items-center space-x-2 ${lang === currentLang ? 'bg-indigo-50 text-primary' : 'text-gray-700'}">
                        <span>${languageFlags[lang]}</span>
                        <span>${languageNames[lang]}</span>
                    </button>
                `).join('')}
            </div>
        </div>
    `;
}

/**
 * Initialize language selector dropdown functionality
 */
function initLanguageSelector() {
    const selector = document.getElementById('language-selector');
    if (!selector) return;

    const button = selector.querySelector('button');
    const dropdown = document.getElementById('language-dropdown');

    if (button && dropdown) {
        button.addEventListener('click', (e) => {
            e.stopPropagation();
            dropdown.classList.toggle('hidden');
        });

        // Close dropdown when clicking outside
        document.addEventListener('click', () => {
            dropdown.classList.add('hidden');
        });
    }
}

/**
 * Initialize i18n system
 */
function initI18n() {
    const currentLang = getCurrentLanguage();
    document.documentElement.lang = currentLang;
    applyTranslations(currentLang);
    initLanguageSelector();
    updateLangSelectorDisplay(currentLang);
}

// Auto-initialize when DOM is ready
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initI18n);
} else {
    initI18n();
}

